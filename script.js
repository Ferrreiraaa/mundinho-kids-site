const qs = (s, el = document) => el.querySelector(s);

function createCard(theme, featured = false) {
  const href = `tema.html?tema=${encodeURIComponent(theme.slug)}`;
  return `
    <a class="${featured ? 'highlight-card' : 'feed-card'}" href="${href}" aria-label="Abrir tema ${theme.name}">
      ${featured ? '<div class="card-badge">Destaque</div>' : ''}
      <img src="${theme.cover}" alt="${theme.name}">
      <div class="card-label">
        <strong>${theme.name}</strong>
        <span>${theme.count} ${theme.count === 1 ? 'foto' : 'fotos'}</span>
      </div>
    </a>
  `;
}

function renderHomePage() {
  const feedGrid = qs('#feedGrid');
  if (!feedGrid) return;

  const searchInput = qs('#searchInput');
  const countEl = qs('#catalogCount');
  const highlightGrid = qs('#highlightGrid');
  const heroMiniGrid = qs('#heroMiniGrid');

  const featuredThemes = TEMAS.filter(t => t.featured).slice(0, 8);
  if (highlightGrid) highlightGrid.innerHTML = featuredThemes.map(t => createCard(t, true)).join('');

  if (heroMiniGrid) {
    heroMiniGrid.innerHTML = featuredThemes.slice(0, 4).map(theme => `
      <a class="hero-mini-item" href="tema.html?tema=${encodeURIComponent(theme.slug)}" aria-label="Abrir tema ${theme.name}">
        <img src="${theme.cover}" alt="${theme.name}">
        <span>${theme.name}</span>
      </a>
    `).join('');
  }

  function renderFeed(list) {
    list = list.sort((a, b) => a.name.localeCompare(b.name, 'pt-BR'));
    countEl.textContent = `${list.length} temas encontrados`;
    if (!list.length) {
      feedGrid.innerHTML = `<div class="empty-state">Nenhum tema encontrado. Digita direito, criatura do carbono.</div>`;
      return;
    }
    feedGrid.innerHTML = list.map(t => createCard(t)).join('');
  }

  renderFeed(TEMAS);

  searchInput.addEventListener('input', () => {
    const term = searchInput.value.trim().toLowerCase();
    const filtered = TEMAS.filter(theme => theme.name.toLowerCase().includes(term));
    renderFeed(filtered);
  });
}

function renderThemePage() {
  const title = qs('#themeTitle');
  if (!title) return;

  const params = new URLSearchParams(window.location.search);
  const slug = params.get('tema');
  const theme = TEMAS.find(t => t.slug === slug) || TEMAS[0];

  document.title = `${theme.name} | Mundinho Kids`;
  qs('#themeTitle').textContent = theme.name;
  qs('#themeDescription').textContent = theme.description;
  qs('#themeCover').src = theme.cover;
  qs('#themeCover').alt = theme.name;
  qs('#themeMeta').innerHTML = `
    <span class="meta-pill">📸 ${theme.count} ${theme.count === 1 ? 'foto' : 'fotos'}</span>
    <span class="meta-pill">🎉 Personagens vivos</span>
    <span class="meta-pill">📍 São José do Rio Preto e Região</span>
  `;

  qs('#themeGallery').innerHTML = theme.images.map((img, index) => `
    <img src="${img}" alt="${theme.name} - foto ${index + 1}">
  `).join('');
}

document.addEventListener('DOMContentLoaded', () => {
  renderHomePage();
  renderThemePage();
});
